}


##' Generate a binary treatment
##'
##' .. content for \details{} ..
##'
##' @param df df generated by generate_covariates
##' @param alphas true coefficients for the logistic treatment generating model
##'
##' @return df with treatment (A) added.
##'
##' @author Kazuki Yoshida
generate_treatment <- function(df, alphas) {

    ## X matrix
    Xs <- as.matrix(df)

    ## Add constant term
    Xs <- cbind(rep(1, nrow(Xs)), Xs)

    ## Linear predictor
    lp <- as.numeric(Xs %*% alphas)

    ## P[A=1|X] (true PS)
    pA <- locfit::expit(lp)

    ## Bernoulli(pA)
    df$A <- rbinom(n = length(pA),
                   size = 1,
                   prob = pA)

    df
}
